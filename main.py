# 1. Импортируем всё необходимое
from flask import Flask, render_template, request

# 2. Создаём приложение
#    Т.к. файл называется main.py, используем такой способ
import os

os.environ['FLASK_APP'] = 'main.py'
app = Flask(__name__)

# 3. Создаём ГЛОБАЛЬНУЮ переменную, которая будет хранить счёт
#    Она живёт на сервере и помнит число, даже если страницу перезагрузить
counter = 0


# 4. Главная страница
@app.route('/', methods=['GET', 'POST'])
def index():
    # Говорим, что будем менять глобальную переменную counter
    global counter

    # Если пользователь нажал кнопку (метод POST)
    if request.method == 'POST':
        # Увеличиваем счётчик на 1
        counter = counter + 1
        # ИЛИ можно написать: counter += 1 (это то же самое)

        # Небольшая защита от дурака: если счётчик стал слишком большим,
        # можно его сбросить (но мы этого не делаем, пусть растёт!)

    # Отправляем пользователю HTML-страницу и передаём туда текущее значение счётчика
    return render_template('index.html', score=counter)


# 5. Запускаем сервер
if __name__ == '__main__':
    app.run(debug=True)